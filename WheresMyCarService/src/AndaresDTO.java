public class VagasDTO {
	
	private String Id;
	private int IdOcupante;
	private String Situacao;
	private Boolean VagaDeficiente;
	
	
	public String getId() {
		return Id;
	}
	public void setId(String id) {
		Id = id;
	}
	public int getIdOcupante() {
		return IdOcupante;
	}
	public void setIdOcupante(int idOcupante) {
		IdOcupante = idOcupante;
	}
	public String getSituacao() {
		return Situacao;
	}
	public void setSituacao(String situacao) {
		Situacao = situacao;
	}
	public Boolean getVagaDeficiente() {
		return VagaDeficiente;
	}
	public void setVagaDeficiente(Boolean vagaDeficiente) {
		VagaDeficiente = vagaDeficiente;
	}
	
	

}
